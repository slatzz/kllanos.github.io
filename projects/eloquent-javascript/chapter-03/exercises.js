// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

// Write a function that returns true if a number is even.

function isEven(number) {
  
 if (number % 2 == 0) {
   return true;
 } else {
   return false;
 }
 
}
